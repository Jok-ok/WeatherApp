import UIKit.UIColor

extension UIColor {
    static var accentColor: UIColor {
        return UIColor(named: "AccentColor")!
    }
    static var backgroundColor: UIColor {
        return UIColor(named: "BackgroundColor")!
    }
    static var buttonFontColor: UIColor {
        return UIColor(named: "ButtonFontColor")!
    }
    static var fontColor: UIColor {
        return UIColor(named: "FontColor")!
    }

    static var accentOp: UIColor {
        return UIColor(named: "AccentOp")!
    }
    
    static var secondaryFont: UIColor {
        return UIColor(named: "SecondaryFontColor")!
    }
}

