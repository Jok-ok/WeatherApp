import UIKit.UIColor

extension UIColor {
    static var accentColor: UIColor {
        return UIColor(named: "Colors/AccentColor")!
    }
    static var backgroundColor: UIColor {
        return UIColor(named: "Colors/BackgroundColor")!
    }
    static var buttonFontColor: UIColor {
        return UIColor(named: "Colors/ButtonFontColor")!
    }
    static var FontColor: UIColor {
        return UIColor(named: "Colors/FontColor")!
    }

    static var accentOp: UIColor {
        return UIColor(named: "Colors/AccentOp")!
    }
}

